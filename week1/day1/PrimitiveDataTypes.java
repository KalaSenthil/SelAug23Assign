package week1.day1;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		int num = 16;
		long longerNum = 1237897789;
		float flNum = 20.5f;
		double dNum = 12.890234;
		boolean isCorrect = true;
		char oneChar = 'C';
		String myStr = "Aim";
		
		System.out.println("Integer number is : "+num);
		System.out.println("Long Type number is: "+longerNum);
		System.out.println("Float Type number is: "+flNum);
		System.out.println("Double Type number is: "+dNum);
		System.out.println("Boolean Type is: "+isCorrect);
		System.out.println("Char value is: "+oneChar);
		System.out.println("String value is: "+myStr);
	
		
		

	}

}
